package loops;

public class for_loop {
	public static void main(String[] args) {
		for (int i=1;i<=5;i++)
			System.out.println(i);
		System.out.println("-----------------------");
		for (int j=1; j>=-5;j--)
			System.out.println(j);
		System.out.println("-----------------------");
		for (int k=1; k<=10;k=k+2)
			System.out.println(k);
		System.out.println("-----------------------");
		for (int g=1;g<=10;g++)
			System.out.println(g*2);
		System.out.println("-----------------------");
		for(int d=1;d<=5;d++)
			System.out.println(d*d);
	}

}
